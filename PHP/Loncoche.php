    <section id="sitios-turisticos">
        <h2>Sitios Turísticos en Loncoche</h2>
        <p>Loncoche al ser una comuna pequeña, no tiene muchos atractivos turísticos, aún así tiene
            algunos paisajes que merecen la pena visitar, éstos son algunas de ellos:</p>
        <ul>
            <b>Cuesta Lastarria</b>
            <li>
                La cuesta lastarria es uno de los lugares más populares de la zona. En esta cuesta se pueden encontrar
                variedad de paisajes, tales como un amplio bosque, cascadas y unos miradores desde los cuales se puede ver toda la ciudad.
                Mucha gente disfruta hacer Trekking en esta zona y además tiene una de las mejores pistas de Mountain Bike en Chile, habiendo 
                aquí competencias de este índole practicamente cada año.    
            </li>
            <img class= "imagenes" src="https://mapio.net/images-p/926693.jpg" alt="Imagen de la Cuesta Lastarria"><hr><br>
            <b>Túnel Afquintue</b>
            <li>
                Es un túnel Ferroviario construido en el sector rural de Afquintue en el año 1902, con 400 metros de longitud aproximadamente,
                6.40 metros de alto y 5 metros de ancho. Estas vías están abandonadas desde el año 2010, haciendo así que este túnel
                se vuelva un lugar perfecto para ir a caminar y disfrutar de las vistas que te brinda la zona.  
            </li>
            <img class= "imagenes" src="https://live.staticflickr.com/3696/14286611264_d3dca9ee95.jpg" alt="Imagen del túnel Afquintue">
        </ul>
    </section>
