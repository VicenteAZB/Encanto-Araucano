    <section id="entretenimiento">
        <h2>Entretenimiento en Loncoche</h2>
        <p>Loncoche ofrece pocos panoramas para poder entretenerse en familia, éstos son los más destacados: </p>
        <ul>
            <b>Festival del rio Cruces</b>
            <li>
                El Festival del rio Cruces es uno de los eventos importantes para la comunidad de Loncoche y para los turistas, éste 
                ya cuenta con 2 versiones, siendo la tercera el próximo 09 de Octubre del año vigente. Se presentan artistas locales y regionales en este evento, 
                que además está marcado por diferentes competencias deportivas y familiares a la orilla del prominente rio que cruza la ciudad de Loncoche.
            </li>
            <img class="imagenes" src="http://www.loncochealdia.cl/wp-content/uploads/2019/10/2-Festiva-1024x576.jpg" alt="Imagen del festival del rio cruces"><hr><br>
            <b>Expo Loncoche</b>
            <li>
                Cada verano se lleva a cabo en Loncoche la Expo Loncoche, un evento que invita a todo Chile a conocer a los emprendedores de la 
                comuna del copihue, de la región y la zona, tanto en gastronomía, agrícultura, ganadería, tecnología, entre otros, permitiendo 
                convertirse en la mejor y más grande exposición del sur de Chile.
                Este evento que dura 6 días también ha contado siempre con artistas de talla nacional e internacional, como Illapu, Los Bunkers,
                Maná, Stefan Kramer, Bombo Fica, entre otros más. <br>
                Este es sin duda un panorama que no te puedes perder si te encuentras en la Región de la Araucanía durante el verano.
            </li>
            <img class= "imagenes" src="https://www.clave9.cl/wp-content/uploads/2023/02/expo-loncoche.jpg" alt="Imagen de la Expo Loncoche"><br>
        </ul>
